    interface TaxiElement {

        public double accept(VehicleVisitor visitor);
    }

    class Customer implements TaxiElement {

        private String name;
        private double destination;

        public Customer(String name, double destination) {
            this.name = name;
            this.destination = destination;
        }

        public String getName() {
            return name;
        }

        public double getDestination() {
            return this.destination;
        }

        @Override
        public double accept(VehicleVisitor visitor) {
            return visitor.visit(this);
        }

    }

    class Vehicle implements TaxiElement {

        private String vehicle;
        private String capacity;
        private double price;

        public Vehicle(String vehicle, String capacity, double cost) {
            this.vehicle = vehicle;
            this.capacity = capacity;
            this.price = cost;
        }

        public String getVehicle() {
            return vehicle;
        }

        public String getCapacity() {
            return capacity;
        }

        public double getPrice() {
            return price;
        }

        @Override
        public double accept(VehicleVisitor visitor) {
            return visitor.visit(this);
        }

    }

    
    interface VehicleVisitor {

        double visit(Customer customer);

        double visit(Vehicle vehicle);

    }

    
    class TaxiVisitorImpl implements VehicleVisitor {

        @Override
        public double visit(Customer customer) {

            String name = customer.getName();
            double destination = customer.getDestination();

            String newline = System.getProperty("line.separator");

            JOptionPane.showMessageDialog(null,
                    "Customer Name : " + name + newline
                    + "Destination Cost : " + destination
            );

            lblCustomer.setText("Customer : " + name);
            lblDestination.setText("Destination : " + destination);
            return destination;
        }

        @Override
        public double visit(Vehicle vehicle) {

            String vehicleType = vehicle.getVehicle();
            String capacity = vehicle.getCapacity();
            double cost = vehicle.getPrice();

            String newline = System.getProperty("line.separator");

            JOptionPane.showMessageDialog(null,
                    "Vehicle Type : " + vehicleType + newline
                    + "Capacity : " + capacity + newline
                    + "Vehicle Charge : " + cost
            );

            lblVehicle.setText("Vehicle Type : " + vehicleType);
            lblCapacity.setText("Capacity : " + capacity);

            return cost;
        }

    }

    private double calculatePrice(TaxiElement[] items) {
        VehicleVisitor visitor = new TaxiVisitorImpl();
        double sum = 0;
        for (TaxiElement item : items) {
            sum = sum + item.accept(visitor);
            lblPrice.setText("Price : " + sum);
        }
        return sum;
    }

