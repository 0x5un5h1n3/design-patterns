    interface Pen {

        public void Color(String color);

        public void refill();
    }

    class InkPen implements Pen {

        //Intrinsic Attribute
        private final String brand;
        private final String length;
        private final double size;

        //Extrinsic Attribute
        private String color;

        public InkPen() {
            brand = FlyweightPenRefills.txtBrand.getText();
            length = FlyweightPenRefills.comboLength.getSelectedItem().toString();
            size = Double.parseDouble(FlyweightPenRefills.txtSize.getText());
        }

        @Override
        public void Color(String color) {
            this.color = color;
        }

        @Override
        public void refill() {

            lblBrand.setText("Brand : " + brand);
            lblLength.setText("Length : " + length);
            lblSize.setText("Size : " + size);
            lblColor.setText("Color : " + color);

            JOptionPane.showMessageDialog(null,
                    "Brand : " + brand
                    + newline + "Length : " + length
                    + newline + "Size : " + size);

            if (!checkRed.isSelected() && !checkBlack.isSelected() && !checkGreen.isSelected()) {
                JOptionPane.showMessageDialog(null, "The Pen has No Ink");
            } else {
                JOptionPane.showMessageDialog(null, "Ink Color : " + color);
            }

        }
    }

