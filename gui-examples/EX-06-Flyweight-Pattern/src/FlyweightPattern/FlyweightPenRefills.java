    private void btnRefillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefillActionPerformed

        if (("".equals(txtBrand.getText())) || ("".equals(txtSize.getText()))) {
            JOptionPane.showMessageDialog(this, "Please Fill Pen Details!");
            txtBrand.grabFocus();

        } else {
            
            Pen p = new InkPen();

            if (checkRed.isSelected()) {
                p.Color("Red");
            }
            if (checkBlack.isSelected()) {
                p.Color("Black");
            }
            if (checkGreen.isSelected()) {
                p.Color("Green");
            }

            p.refill();
            if (!checkRed.isSelected() && !checkBlack.isSelected() && !checkGreen.isSelected()) {
                JOptionPane.showMessageDialog(this, "Refill Failed");
            } else {
                JOptionPane.showMessageDialog(this, "Refill Successful!");
            }
        }

    }//GEN-LAST:event_btnRefillActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        txtBrand.setText("");
        comboLength.setSelectedIndex(0);
        txtSize.setText("");

        lblBrand.setText("Brand : N/A");
        lblLength.setText("Length : N/A");
        lblSize.setText("Size : N/A");
        lblColor.setText("Color : N/A");

        checkRed.setSelected(false);
        checkBlack.setSelected(false);
        checkGreen.setSelected(false);

        pnlRed.setBackground(new Color(102, 102, 102));
        pnlBlack.setBackground(new Color(102, 102, 102));
        pnlGreen.setBackground(new Color(102, 102, 102));

        JOptionPane.showMessageDialog(this, "All Fields Cleared!");
    }//GEN-LAST:event_btnClearActionPerformed

