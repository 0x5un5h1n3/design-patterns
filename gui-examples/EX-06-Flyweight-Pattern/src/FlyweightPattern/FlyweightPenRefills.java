    private void txtSizeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSizeKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            if (c != '.') {
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtSizeKeyTyped

